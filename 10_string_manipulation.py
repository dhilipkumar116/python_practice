print(s[0:2]) # length 2 from 1st character
print(s[:5]) # length 5
print(s[1:]) # from 2nd character to last
print(s[-1]) # prints last character
print(s[-2]) # prints 2nd last character
print(s[:-1]) # prints except last character
print(s[::-1]) # reversing string